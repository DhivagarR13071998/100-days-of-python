print("Welcome to the rollercoaster!")
height = int(input("What is your height in cm? "))

# EQUAL TO : ==
if height == 120:
    print("you can ride the rollercoaster")
else :
    print("Sorry your height is not enough to ride the rollercoaster")

# NOT EQUAL TO : !=
if height != 120:
    print("you can ride the rollercoaster")
else :
    print("Sorry your height is not enough to ride the rollercoaster")

# Less than or equal to : <=
if height <= 120:
    print("you can ride the rollercoaster")
else :
    print("Sorry your height is not enough to ride the rollercoaster")

# Greater than or equal to : >=
if height >= 120:
    print("you can ride the rollercoaster")
else :
    print("Sorry your height is not enough to ride the rollercoaster")

# Less than : <
if height < 120:
    print("you can ride the rollercoaster")
else :
    print("Sorry your height is not enough to ride the rollercoaster")